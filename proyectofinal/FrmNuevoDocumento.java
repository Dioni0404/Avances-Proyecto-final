    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String codEmp = this.txtCodEmpleado.getText();
        String areaEmp = String.valueOf(this.cboAreaEmp.getSelectedItem());
        Empleado oEmpleado = new Empleado(codEmp,areaEmp);
        registroEmpleados.agregarElemento(oEmpleado);
        
        String prioridad = String.valueOf(this.spnPrioridad.getValue());
        String codDoc = this.txtCodDocumento.getText();
        String tipo = String.valueOf(this.cboTipoDoc.getSelectedItem());
        String asunto = this.txtAsunto.getText();
        String estado = String.valueOf(this.cboEstado.getSelectedItem());
        //Fecha de Registro:
        int dia = Integer.valueOf(String.valueOf(this.spnDia.getValue()));
        int mes = this.cboMes.getSelectedIndex()+1;
        int año = Integer.valueOf(String.valueOf(this.spnAño.getValue()));
        Fecha fechaReg = new Fecha(dia,mes,año);
        String codUsuario = this.txtCodUser.getText();
        String empDestino = this.txtEmpleadoDestino.getText();
        String areaDestino = String.valueOf(this.cboAreaDestino.getSelectedItem());
        Expediente oExpediente = new Expediente(prioridad, codDoc, tipo, asunto, estado, fechaReg.toString(),
        codUsuario, empDestino, areaDestino);
        expedientes.agregarElemento(oExpediente);
        
        
        NodoHistorial<String> nodoH = new NodoHistorial<>(codDoc, asunto, estado, codUsuario, fechaReg.toString(), empDestino, areaDestino, null);
        historial.agregarElemento(nodoH);
        historialEmp.agregar(nodoH);
        JOptionPane.showMessageDialog(this, "El documento se registró correctamente.");
        
        this.txtCodEmpleado.setText(null);
        this.cboAreaEmp.setSelectedIndex(0);
        this.txtCodDocumento.setText(null);
        this.cboTipoDoc.setSelectedIndex(0);
        this.txtAsunto.setText(null);
        this.cboEstado.setSelectedIndex(0);
        this.txtCodUser.setText(null);
        this.txtEmpleadoDestino.setText(null);
        this.cboAreaDestino.setSelectedIndex(0);
        
    }//GEN-LAST:event_btnGuardarActionPerformed

